  </div>
</template>

<script>
import Vue from "vue";
import { Toast, InfiniteScroll } from "mint-ui";
Vue.use(InfiniteScroll);
export default {
  name: "Header",
  components: {},
  data() {
    return {
      options: ["综合最佳", "收藏最多", "做过最多"],
      now: 0,
      list: [],
      pageNo: 1,
      cont: null,
      type: "综合最佳",
      isok: false
    };
  },
  methods: {
    show() {
      this.list = [];
      this.$router.push({ path: "list", query: { searchKey: this.cont } });
      this.getData();
    },
    selItem(index, type) {
      this.now = index;
      this.type = type;
      this.list = [];
      this.getData();
    },
    loadMore() {
      this.getData();
    },
    getData() {
      this.$axios
        .get("/api/fx2/cookbook/bookSearch", {
          params: {
            type: this.type,
            pageNo: this.pageNo,
            searchKey: this.cont
          }
        })
        .then(res => {
          this.list = this.list.concat(res.pager.data);
          this.pageNo++;
          if (this.list.length == 0) {
            this.isok = !this.isok;
          } else {
            this.isok = false;
          }
          console.log(this.list.length);
        })
        .catch(err => {
          console.log(err);
        });
    }
  },
  created() {
    //   this.getData()
    this.cont = this.$route.query.searchKey;
  },
  updated() {
    // this.getData()
  },
  beforeUpdate() {
    //  this.getData()
  },
  destroyed() {
    //    delete this.getData()
  }
};
</script>

