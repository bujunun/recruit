        :key="index"
      >
        <div class="img">
          <img :src="item.bookImg" alt>
        </div>
        <div class="title">
          <div class="name">{{item.bookName}}</div>
          <div class="desc confs">{{item.bookData}}</div>
          <div class="user confs">
